package domain;

import java.sql.Date;

/**
 *
 * @author laboratorio
 */
public class Conta {

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public Date getDataCadastro() {
        return dataCadastro;
    }

    public void setDataCadastro(Date dataCadastro) {
        this.dataCadastro = dataCadastro;
    }

    public Conta(){
    }
    
    public Conta(float valor, int tipo) {
        this.valor = valor;
        this.tipo = tipo;
    }
    
    private int id;
    private float valor;
    private int tipo;
    private Date dataCadastro;
}
